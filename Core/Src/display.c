void fsm_simple_buttons_run () {
	switch(mode){
		case MODE_INIT:
			setTimer0(1000);
			mode = MODE_1;
		break;
		case MODE_1:
			display7SEG(counter);
			if(timer0_flag == 1){
				if(counter > 0){
					counter--;
					setTimer0(1000);
				}
			}
			if(is_Button0_Pressed()){
				counter = 0;
				setTimer0(10000);
				mode = 2;
			}
			if(is_Button1_Pressed()){
				counter ++;
				setTimer0(10000);
				mode = 2;
			}
			if(is_Button2_Pressed()){
				counter --;
				setTimer0(10000);
				mode = 2;
			}
		break;
		case MODE_2:
			display7SEG(counter);
			if(is_Button0_Pressed()){
				counter = 0;
				setTimer0(10000);
			}
			if(is_Button1_Pressed()){
				counter++;
				if(counter > 9) counter = 0;
				setTimer0(10000);
			}
			if(is_Button2_Pressed()){
				counter--;
				if(counter < 0) counter = 9;
				setTimer0(10000);
			}
			if(timer0_flag == 1){
				mode = MODE_INIT;
			}
		break;
	}
}
void led_blinky (){
	switch(mode_led){
	case MODE_LED_INIT:
		setTimer3(500);
		mode_led = MODE_LED_BLINKY;
	break;
	case MODE_LED_BLINKY:
		if(timer3_flag == 1){
			HAL_GPIO_TogglePin(LED_RED_GPIO_Port, LED_RED_Pin);
			setTimer3(500);
		}
	break;
	}
}
